#Operadores de Asignacion

mi_numero = 20
print(mi_numero)

#Operador +=
mi_numero += 5
print(mi_numero)

#Operador -=
mi_numero -= 3
print(mi_numero)

#Operador *= 
mi_numero *= 2
print(mi_numero)

#Operador /= 
mi_numero /= 4
print(mi_numero)

#Operador %=
mi_numero %= 2
print(mi_numero)

#Operador **=
numero = 2
numero **= 3
print(numero)