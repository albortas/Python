# Operadores Relacionales

a = 5
b = 6

# Operador igualdad
print(f'{a} == {b} -> {a == b}')

#Operador distinto
print(f'{a} != {b} -> {a != b}')

# Operador menor que
print(f'{a} < {b} -> {a < b}')

# Operador menor o igual que
print(f'{a} <= {b} -> {a <= b}')

# Operador mayor que
print(f'{a} > {b} -> {a > b}')

# Operador mayor o igual que
print(f'{a} >= {b} -> {a >= b}')