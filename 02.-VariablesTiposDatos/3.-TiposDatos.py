print(mi_entero,type(mi_entero), hex(id(mi_entero)))

mi_flotante = 7.9
print(mi_flotante,type(mi_flotante), hex(id(mi_flotante)))

mi_cadena = 'Hola'
print(mi_cadena,type(mi_cadena),hex(id(mi_cadena)))

# Tipo bool
mi_boleano = True
print(mi_boleano,type(mi_boleano), hex(id(mi_boleano)))

# Ausencia de Valor (None)
mi_none = None
print(mi_none,type(mi_none), hex(id(mi_none)))

