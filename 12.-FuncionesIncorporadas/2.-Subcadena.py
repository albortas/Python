#Subcadena
cadena = 'Hola Mundo'
# No incluye el indice final (indice final + 1)
subcadena1 = cadena[0: 10]
print(subcadena1)

subcadena2 = cadena[0: 4]
print(subcadena2)

# subcadena de Mundo
subcadena3 = cadena[5:]
print(subcadena3)

subcadena4 = cadena[5: 10]
print(subcadena4)

subcadena5 = cadena[:]
print(subcadena5)